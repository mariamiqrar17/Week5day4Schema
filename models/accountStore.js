const mongoose = require('mongoose');

const storeAccountSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

const StoreAccount = mongoose.model('StoreAccount', storeAccountSchema);

module.exports = StoreAccount;
